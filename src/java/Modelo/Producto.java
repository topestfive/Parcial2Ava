package Modelo;

/**
 *
 * @author Usuario
 */
public class Producto {
    private int id;
    private String nom,precio,stock,estado;

    public Producto() {
    }

    public Producto(int id, String nom, String precio, String stock, String estado) {
        this.id = id;
        this.nom = nom;
        this.precio = precio;
        this.stock = stock;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    @Override
    public String toString() {
        return "Producto{" + "id=" + id + ", nom=" + nom + ", precio=" + precio + ", stock=" + stock + ", estado=" + estado + '}';
    }
    
}
